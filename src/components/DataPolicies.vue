        </p>
        <br>
        <ul class="px-5">
            <li>
                <p><strong>Principio de legalidad en materia de Tratamiento de datos:</strong> El Tratamiento a que se
                    refiere la presente ley es una actividad reglada que debe sujetarse a lo establecido en ella y en
                    las demás disposiciones que la desarrollen;</p>
            </li>
            <li>
                <p><strong>Principio de finalidad:</strong> El Tratamiento debe obedecer a una finalidad legítima de
                    acuerdo con la Constitución y la Ley, la cual debe ser informada al Titular;</p>
            </li>
            <li>
                <p><strong>Principio de libertad:</strong> El Tratamiento sólo puede ejercerse con el consentimiento,
                    previo, expreso e informado del Titular. Los datos personales no podrán ser obtenidos o divulgados
                    sin previa autorización, o en ausencia de mandato legal o judicial que releve el consentimiento;</p>
            </li>
            <li>
                <p><strong>Principio de veracidad o calidad:</strong> La información sujeta a Tratamiento debe ser
                    veraz, completa, exacta, actualizada, comprobable y comprensible. Se prohíbe el Tratamiento de datos
                    parciales, incompletos, fraccionados o que induzcan a error;</p>
            </li>
            <li>
                <p><strong>Principio de transparencia:</strong> En el Tratamiento debe garantizarse el derecho del
                    Titular a obtener del Responsable del Tratamiento o del Encargado del Tratamiento, en cualquier
                    momento y sin restricciones, información acerca de la existencia de datos que le conciernan;</p>
            </li>
            <li>
                <p><strong>Principio de acceso y circulación restringida:</strong> El Tratamiento se sujeta a los
                    límites que se derivan de la naturaleza de los datos personales, de las disposiciones de la presente
                    ley y la Constitución. En este sentido, el Tratamiento sólo podrá hacerse por personas autorizadas
                    por el Titular y/o por las personas previstas en la presente ley;</p>
                <p>Los datos personales, salvo la información pública, no podrán estar disponibles en Internet u otros
                    medios de divulgación o comunicación masiva, salvo que el acceso sea técnicamente controlable para
                    brindar un conocimiento restringido sólo a los Titulares o terceros autorizados conforme a la
                    presente ley;</p>
            </li>
            <li>
                <p><strong>Principio de seguridad:</strong> La información sujeta a Tratamiento por el Responsable del
                    Tratamiento o Encargado del Tratamiento a que se refiere la presente ley, se deberá manejar con las
                    medidas técnicas, humanas y administrativas que sean necesarias para otorgar seguridad a los
                    registros evitando su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento;</p>
            </li>
            <li>
                <p><strong>Principio de confidencialidad:</strong> Todas las personas que intervengan en el Tratamiento
                    de datos personales que no tengan la naturaleza de públicos están obligadas a garantizar la reserva
                    de la información, inclusive después de finalizada su relación con alguna de las labores que
                    comprende el Tratamiento, pudiendo sólo realizar suministro o comunicación de datos personales
                    cuando ello corresponda al desarrollo de las actividades autorizadas en la presente ley y en los
                    términos de la misma.</p>
            </li>
